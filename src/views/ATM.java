    private void depositBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositBtnActionPerformed
        
        double depositAmount = 0;
        
        if(depositField.getText().equals("")) {
            
            depositAlert.setText("Please Enter Amount !");
        
        }else {
        
            depositAmount = Double.parseDouble(depositField.getText());
            
        
        }
        
        
        if(!depositField.getText().equals("")) {
            
            jdbc = new JDBC();
            
            double balance = jdbc.getAccountBalance(activeAccount.getAccNumber());
            
            jdbc = null;
            
            jdbc = new JDBC();
            
            jdbc.depositMoney(depositAmount, activeAccount.getAccNumber(), balance);
            
            jdbc = null;
            
            jdbc = new JDBC();
            
            //Timestamp transactionDate, int accountId, double amount, String transactionType
            long time = System.currentTimeMillis();
            java.sql.Timestamp transactionDate = new java.sql.Timestamp(time);
            Transaction tr = new Transaction(transactionDate, activeAccount.getAccNumber(), depositAmount, "Deposit");
            
            int success = jdbc.addTransaction(tr);
            
            if(success > 0) {
                JOptionPane.showMessageDialog(null, "Deposit Successfull ! New Balance: " + (balance + depositAmount));
                depositField.setText("");
            }
                
        }
    }//GEN-LAST:event_depositBtnActionPerformed

