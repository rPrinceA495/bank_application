    private void withdrawalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawalBtnActionPerformed
        
        String withdrawal = "";
        double withdrawalAmount = 0;
        
        btn200.setActionCommand("200");
        btn100.setActionCommand("100");
        btn50.setActionCommand("50");
        btn20.setActionCommand("20");
        btn10.setActionCommand("10");
        btnOtherAmnt.setActionCommand(withdrawalField.getText());
        
        if(withdrawalField.getText().equals("")) {
            
            withdrawal = buttonGroup1.getSelection().getActionCommand();
        
        }else {
        
            withdrawal = withdrawalField.getText();
        
        }
        
        
        
        if(withdrawal.equals("")) {
            
            withdrawAlert.setText("Enter Amount !");
        
        }else {
        
            withdrawalAmount = Double.parseDouble(withdrawal);
            
        }
        
        if(!withdrawal.equals("")) {
            
            jdbc = new JDBC();
            
            double balance = jdbc.getAccountBalance(activeAccount.getAccNumber());
            
            // Check if funds are sufficient:
            if(withdrawalAmount > balance) {
                JOptionPane.showMessageDialog(null, "Account Balance Insufficient ! Current Balance: " + balance );
                withdrawalField.setText("");

            }else {
            
                jdbc = null;
            
                jdbc = new JDBC();

                jdbc.withdrawMoney(withdrawalAmount, activeAccount.getAccNumber(), balance);

                jdbc = null;

                jdbc = new JDBC();

                //Timestamp transactionDate, int accountId, double amount, String transactionType
                long time = System.currentTimeMillis();
                java.sql.Timestamp transactionDate = new java.sql.Timestamp(time);
                Transaction tr = new Transaction(transactionDate, activeAccount.getAccNumber(), withdrawalAmount, "Withdrawal");

                int success = jdbc.addTransaction(tr);

                if(success > 0) {
                    JOptionPane.showMessageDialog(null, "Withdrawal Successfull ! New Balance: " + (balance - withdrawalAmount));
                    withdrawalField.setText("");
                }
                
            }
            
            
            
        }
    }//GEN-LAST:event_withdrawalBtnActionPerformed

